#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char	*cs1;

	cs1 = s1;
	while (*s1)
		s1++;
	while (len--)
	{
		*s1++ = *s2++;
		if (!(*s1))
			return (cs1);
	}
	*s1 = '\0';
	return (cs1);
}
