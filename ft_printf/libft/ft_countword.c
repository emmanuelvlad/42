#include "libft.h"

int	ft_countword(const char *s)
{
	int	word;

	word = 0;
	while (*s)
	{
		if (*s != ' ' || *s != '\n' || *s != '\t')
		{
			word++;
			while (*s)
			{
				if (*s == ' ' || *s != '\n' || *s != '\t')
					break ;
				else
					s++;
			}
		}
		else
			s++;
	}
	return (word);
}
