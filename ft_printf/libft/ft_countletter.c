#include "libft.h"

int	ft_countletter(const char *s)
{
	int	letter;

	letter = 0;
	while (*s++)
	{
		letter++;
		if (*s == ' ' || *s == '\t' || *s == ' ')
			break ;
	}
	return (letter);
}
