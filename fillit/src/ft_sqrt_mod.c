#include "header.h"

int		ft_sqrt_mod(int total_tetri)
{
	int i;

	i = 0;
	while (i < total_tetri * 4)
	{
		if (i * i >= total_tetri * 4)
			break ;
		i++;
	}
	return (i);
}
