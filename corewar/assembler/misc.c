#include "../includes/asm.h"

unsigned int	ft_pow(int x, int y)
{
	int		a;

	a = 1;
	if (y == 0)
		return (1);
	if (y == 1)
		return (x);
	while (y--)
		a = a * x;
	return (a);
}
