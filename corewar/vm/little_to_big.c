#include "../includes/vm.h"

unsigned int	little_to_big(unsigned int b)
{
	b = ((b >> 24) & 0xFF) | ((b >> 8) & 0xFF00) |
		((b << 8) & 0xFF0000) | ((b << 24) & 0xFF000000);
	return (b);
}
